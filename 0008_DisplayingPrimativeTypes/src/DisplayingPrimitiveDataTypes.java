public class DisplayingPrimitiveDataTypes {
	public static void main(String [] args)
	{
		// As you develop and later debug code, you will
		// find it necessary to display values of variables
		// at different locations in your program
		short s = 15431;
		int i = 123456;
		float f = 3.14159f;
		
		// With Concatenation (+), you can automatically 
		// convert and append its operands to strings
		System.out.println("short: " + s + " int: " + i + " float: " + f);
	}	
}
