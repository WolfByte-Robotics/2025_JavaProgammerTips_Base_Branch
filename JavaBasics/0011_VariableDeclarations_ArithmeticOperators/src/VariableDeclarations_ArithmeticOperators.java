public class VariableDeclarations_ArithmeticOperators {

	public static void main (String [] args)
	{
		// When you declare variables in Java, you can assign
		// initial values to the variables. As it turns out
		// you may use an arithmetic expression as an assignment
		float circumference = 2f * 3.14f * 3.4f;
		
		System.out.println("The circumference is " + circumference);	
	}
}
